package ru.nsu.fit;

public class StaticMethod {
    public static double plus(double a, double b) {
        return a + b;
    }
    public static double plus(double a) {
        return a + 1;
    }
    public static int plus(int a, int b) {
        return a + b;
    }
    public static int sub(int a, int b) {
        return a - b;
    }
}
